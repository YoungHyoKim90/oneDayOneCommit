package M2023_12;

public class ConditionalExample {
    public static void main(String[] args) {
        int number = 10;

        // if 조건문
        if (number > 0) {
            System.out.println("양수입니다.");
        } else if (number < 0) {
            System.out.println("음수입니다.");
        } else {
            System.out.println("0입니다.");
        }
    }
}

