package M2024_01;

public class D_0101DynamicCodeExecution {
    public static void main(String[] args) {
        boolean condition = true;
        
        // 조건에 따라 동적으로 코드 실행
        if (condition) {
            System.out.println("조건이 참입니다.");
        } else {
            System.out.println("조건이 거짓입니다.");
        }
    }
}

