package M2024_01;

public class D_0101OppositeCondition {
    public static void main(String[] args) {
        boolean condition = true;
        
        // 조건의 반대를 검사
        if (!condition) {
            System.out.println("조건이 거짓입니다.");
        } else {
            System.out.println("조건이 참입니다.");
        }
    }
}

